# ----------------------------------------------------------
# Вспомогательная функция: собрать сценарий из основы + случайные факты
# ----------------------------------------------------------
def build_script(base_list, facts_list, num_facts=6):
    """
    Берёт случайную базу и добавляет num_facts случайных фактов,
    перемежая их естественными связками.
    """
    base = random.choice(base_list)
    # Выбираем уникальные факты (без повторений в одном видео)
    facts = random.sample(facts_list, min(num_facts, len(facts_list)))

    # Вступления для первого факта
    intro_first = [
        "А вы знали? ",
        "Интересный факт: ",
        "Вот вам факт: ",
        "Вы когда-нибудь задумывались, что ",
        "Мало кто знает, но ",
        "Удивительно, но ",
        "",
        "Кстати, "
    ]
    # Вступления для последующих фактов (кроме последнего)
    intro_middle = [
        "Также ",
        "Кроме того, ",
        "Ещё ",
        "Важно отметить, что ",
        "Не менее интересно, что ",
        "А вот ещё: ",
        "Также известно, что ",
        "",
        "И ещё один момент: "
    ]
    # Вступления для последнего факта
    intro_last = [
        "И напоследок: ",
        "Наконец, ",
        "И ещё: ",
        "Завершу вот чем: ",
        "И в заключение: ",
        ""
    ]

    fact_texts = []
    for i, fact in enumerate(facts):
        if i == 0:
            intro = random.choice(intro_first)
        elif i == len(facts) - 1:
            intro = random.choice(intro_last)
        else:
            intro = random.choice(intro_middle)
        # Если intro пустая, просто добавляем факт без вступления
        fact_texts.append(intro + fact if intro else fact)

    facts_paragraph = "\n\n".join(fact_texts)

    full_text = f"{base}\n\n{facts_paragraph}\n\nА вам было интересно? Подписывайтесь на канал, чтобы не пропускать новые видео!"
    return full_text

